<?php

/*
    ---------- Operadores de incremento ou decremento ----------
*/

// Simplificação da soma ou subtração rápida de uma unidade no valor

// Atribuindo valor zero a variável
$valor = 0;

// Pré incremento
// Nesse exemplo, incrementa e depois imprime
echo ++$valor;

//Pós incremento
// Nesse exemplo, imprime e depois incrementa 
echo $valor--;
echo '<br>';
echo $valor;

// Pré decremento
// Nesse caso, decrementa e depois imprime
echo --$valor;

// Pós decremento
// Imprime e depois decrementa;
echo $valor--;
echo '<br>';
echo $valor;