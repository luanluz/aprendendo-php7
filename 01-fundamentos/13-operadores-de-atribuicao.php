<?php

/*
    ---------- Operadores de atribuição ----------
*/

// A variável vai ser definida utilizando 
// o operador de atribuição e em seguida o valor
// especificado
$variavel = 10;
echo $variavel;
echo '<br>';

// Podemos utilizar um operador aritmético e o operador
// de atribuicao para simplificar a operação
// como no exemplo abaixo
$variavel += 10;
echo $variavel;
echo '<br>';

// É válido para todas as operações aritméticas
$variavel -= 10;
echo $variavel;
echo '<br>';

$variavel *= 10;
echo $variavel;
echo '<br>';

$variavel /= 10;
echo $variavel;
echo '<br>';

$variavel %= 10;
echo $variavel;
echo '<br>';